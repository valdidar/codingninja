package fundamentals;

public class relationAndLogicalOperators {
	public static void main(String[] args) {
		double a1 = 6 / 4;
        int b1  = 6 / 4;
        double c1 = a1 + b1;
        System.out.println(c1);
        double a = 55.5;
        int b = 55;
        a = a % 10;
        b = b % 10;
        System.out.println(a + " "  + b);
	}
}
